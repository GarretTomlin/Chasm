fn inferReturnType(f: IrFunction) u32 {
    for (f.instrs) |instr| {
        switch (instr) {
            .ret => |t| {
                if (t < f.temps.len) return f.temps[t].type_id;
                return 1; // T_INT fallback
            },
            else => {},
        }
    }
    return 6; // T_VOID
}

/// C type string for a TypeId.
fn cTypeOf(type_id: u32) []const u8 {
    return switch (type_id) {
        0 => "int64_t",      // T_UNKNOWN — default to int
        1 => "int64_t",      // T_INT
        2 => "double",       // T_FLOAT
        3 => "bool",         // T_BOOL
        4 => "const char*",  // T_STRING
        5 => "const char*",  // T_ATOM
        6 => "void",         // T_VOID
        7 => "ChasmArray",   // T_ARRAY
        else => "int64_t",   // user-defined: placeholder
    };
}

fn cBinaryOp(op: BinaryOp) []const u8 {
    return switch (op) {
        .add   => "+",
        .sub   => "-",
        .mul   => "*",
        .div   => "/",
        .mod   => "%",
        .eq    => "==",
        .neq   => "!=",
        .lt    => "<",
        .lte   => "<=",
        .gt    => ">",
        .gte   => ">=",
        .@"and" => "&&",
        .@"or"  => "||",
    };
}

fn cUnaryOp(op: UnaryOp) []const u8 {
    return switch (op) {
        .neg => "-",
        .not => "!",
    };
}

