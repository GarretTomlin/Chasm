// Public API
// ---------------------------------------------------------------------------

/// Emit a complete C99 translation unit for `module`.
/// Set `is_imported` to true when emitting a dependency module: skips
/// `chasm_module_init` and `chasm_reload_migrate` to avoid duplicate symbols.
pub fn emitModule(module: IrModule, writer: anytype) !void {
    try emitModuleOpts(module, false, "chasm_rt.h", writer);
}

pub fn emitModuleRaylib(module: IrModule, writer: anytype) !void {
    try emitModuleOpts(module, false, "chasm_rl.h", writer);
}

pub fn emitModuleImported(module: IrModule, writer: anytype) !void {
    try emitModuleOpts(module, true, "chasm_rt.h", writer);
}

fn emitModuleOpts(module: IrModule, is_imported: bool, runtime_header: []const u8, writer: anytype) !void {
    try writer.print(
        \\/* Generated by Chasm compiler — do not edit. */
        \\
        , .{});
    try writer.print("#include \"{s}\"\n\n", .{runtime_header});

    // ---- Enum variant #defines ---------------------------------------------
    if (module.enum_variants.len > 0) {
        try writer.print("/* Enum variants */\n", .{});
        for (module.enum_variants) |ev| {
            try writer.print("#define CHASM_ENUM_{s}_{s} {d}\n",
                .{ ev.enum_name, ev.variant_name, ev.index });
        }
        try writer.print("\n", .{});
    }

    // ---- Extern function comments ------------------------------------------
    if (module.extern_fns.len > 0) {
        try writer.print("/* Extern functions */\n", .{});
        for (module.extern_fns) |ef| {
            try writer.print("// extern: {s}\n", .{ef.c_name});
        }
        try writer.print("\n", .{});
    }

    // ---- Global attribute storage ------------------------------------------
    if (module.attrs.len > 0) {
        try writer.print("/* Module attributes */\n", .{});
        for (module.attrs) |a| {
            try writer.print("static {s} g_{s};\n",
                .{ cTypeOf(a.type_id), a.name });
        }
        try writer.print("\n", .{});
    }

    // ---- Attribute initialiser function (skipped for imported modules) ------
    if (!is_imported) {
        try writer.print("/* Attribute initialiser — call once at module load */\n", .{});
        try writer.print("void chasm_module_init(ChasmCtx *ctx) {{\n", .{});
        if (module.attr_init_instrs.len > 0) {
            try emitTempDeclsForSlice(module.attr_init_temps, writer);
            for (module.attr_init_instrs) |instr|
                try emitInstr(instr, module.attr_init_temps, module.extern_fns, writer, 1);
            // Write computed init values into global storage.
            for (module.attrs) |attr| {
                try writer.print("    g_{s} = t{d};\n", .{ attr.name, attr.init_temp });
            }
        } else {
            try writer.print("    (void)ctx;\n", .{});
        }
        try writer.print("}}\n\n", .{});
    }

    // ---- Forward declarations ----------------------------------------------
    if (module.functions.len > 0) {
        try writer.print("/* Forward declarations */\n", .{});
        for (module.functions) |f| try emitForwardDecl(f, writer);
        try writer.print("\n", .{});
    }

    // ---- Imported module forward declarations ------------------------------
    if (module.imported_fwd_decls.len > 0) {
        try writer.print("/* Imported module forward declarations */\n", .{});
        for (module.imported_fwd_decls) |fwd| {
            try writer.print("{s} chasm_{s}(ChasmCtx *ctx", .{ fwd.ret_c_type, fwd.name });
            for (fwd.param_c_types) |pt| {
                try writer.print(", {s}", .{pt});
            }
            try writer.print(");\n", .{});
        }
        try writer.print("\n", .{});
    }

    // ---- Function definitions ----------------------------------------------
    for (module.functions) |f| try emitFunction(f, module.extern_fns, writer);

    // ---- Hot-reload migration stub (skipped for imported modules) ----------
    if (is_imported) return;
    // Build a pseudo-report treating all attrs as preserved (first compile).
    var alloc_buf: [4096]u8 = undefined;
    var fba = std.heap.FixedBufferAllocator.init(&alloc_buf);
    var pseudo_diffs = std.ArrayListUnmanaged(reload.AttrDiff){};
    for (module.attrs) |a| {
        pseudo_diffs.append(fba.allocator(), .{
            .name         = a.name,
            .compat       = .preserved,
            .old_type_id  = a.type_id,
            .new_type_id  = a.type_id,
            .old_lifetime = a.lifetime,
            .new_lifetime = a.lifetime,
        }) catch break;
    }
    const stub_report = reload.ReloadReport{
        .diffs       = pseudo_diffs.items,
        .safe_reload = true,
        .preserved   = @intCast(pseudo_diffs.items.len),
        .lost = 0, .added = 0, .removed = 0,
    };
    try reload.emitMigrationFn(stub_report, writer);
}

