test "forward declarations emitted before definitions" {
    var tc = try TestCodegen.init(
        \\defp helper() :: int do
        \\  1
        \\end
        \\def caller() do
        \\  helper()
        \\end
    );
    defer tc.deinit();
    const out = tc.output();
    // Forward decl must appear before function body.
    const fwd  = std.mem.indexOf(u8, out, "Forward declarations") orelse return error.Missing;
    const defn = std.mem.indexOf(u8, out, "chasm_helper(ChasmCtx") orelse return error.Missing;
    try testing.expect(fwd < defn);
}

test "full hello.chasm-style module emits C" {
    var tc = try TestCodegen.init(
        \\@score :: script = 0
        \\@high_score :: persistent = 0
        \\defp compute_delta(dt :: f32) :: f32 do
        \\  speed :: frame = 9.8
        \\  speed * dt
        \\end
        \\def on_tick(dt :: f32) do
        \\  delta :: frame = compute_delta(dt)
        \\  saved :: script = copy_to_script(delta)
        \\  @score = saved
        \\end
        \\def on_save() do
        \\  final :: persistent = persist_copy(@score)
        \\  @high_score = final
        \\end
    );
    defer tc.deinit();
    try testing.expect(tc.has("g_score"));
    try testing.expect(tc.has("g_high_score"));
    try testing.expect(tc.has("chasm_compute_delta"));
    try testing.expect(tc.has("chasm_on_tick"));
    try testing.expect(tc.has("chasm_on_save"));
    try testing.expect(tc.has("frame -> script"));
    try testing.expect(tc.has("script -> persistent"));
}
