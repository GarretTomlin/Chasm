fn emitForwardDecl(f: IrFunction, writer: anytype) !void {
    const ret = inferReturnType(f);
    try writer.print("{s} chasm_{s}(ChasmCtx *ctx",
        .{ cTypeOf(ret), f.name });
    for (f.params) |p| {
        try writer.print(", {s} t{d}", .{ cTypeOf(p.type_id), p.temp_id });
    }
    try writer.print(");\n", .{});
}

fn emitFunction(f: IrFunction, extern_fns: []const ir.IrExternFn, writer: anytype) !void {
    const ret = inferReturnType(f);

    // Signature
    try writer.print("{s} chasm_{s}(ChasmCtx *ctx",
        .{ cTypeOf(ret), f.name });
    for (f.params) |p| {
        try writer.print(", {s} t{d}", .{ cTypeOf(p.type_id), p.temp_id });
    }
    try writer.print(") {{\n", .{});

    // Declare all non-param temps up front so gotos are valid C99.
    try emitTempDeclsForFn(f, writer);

    // Instructions
    for (f.instrs) |instr| try emitInstr(instr, f.temps, extern_fns, writer, 1);

    try writer.print("}}\n\n", .{});
}

/// Declare every non-param temp at the top of the function body.
fn emitTempDeclsForFn(f: IrFunction, writer: anytype) !void {
    // Collect param temp ids so we can skip them.
    var param_set: [256]bool = [_]bool{false} ** 256;
    for (f.params) |p| {
        if (p.temp_id < param_set.len) param_set[p.temp_id] = true;
    }
    for (f.temps) |t| {
        if (t.id < param_set.len and param_set[t.id]) continue;
        if (t.type_id == 6) continue; // T_VOID — no variable needed
        try writer.print("    {s} t{d};\n", .{ cTypeOf(t.type_id), t.id });
    }
    if (f.temps.len > f.params.len) try writer.print("\n", .{});
}

fn emitTempDeclsForSlice(temps: []const Temp, writer: anytype) !void {
    for (temps) |t| {
        if (t.type_id == 6) continue;
        try writer.print("    {s} t{d};\n", .{ cTypeOf(t.type_id), t.id });
    }
    if (temps.len > 0) try writer.print("\n", .{});
}

