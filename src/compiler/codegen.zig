const testing  = std.testing;
const ast_mod  = @import("ast");
const sema_mod = @import("sema");
const ir_mod   = @import("ir");
const lower_mod = @import("lower");
const Lexer    = @import("lexer").Lexer;
const Parser   = @import("parser").Parser;
const DiagList = @import("diag").DiagList;
const AstPool  = ast_mod.AstPool;
const Sema     = sema_mod.Sema;
const Lowerer  = lower_mod.Lowerer;

const TestCodegen = struct {
    arena:  std.heap.ArenaAllocator,
    out:    []const u8,

    fn init(src: []const u8) !TestCodegen {
        var arena = std.heap.ArenaAllocator.init(testing.allocator);
        const alloc = arena.allocator();

        var lex    = Lexer.init(src);
        const toks = try lex.tokenize(alloc);
        var pool   = AstPool.init(alloc);
        var diags  = DiagList.init(alloc);
        var parser = Parser.init(toks, &pool, &diags, alloc);
        const top  = try parser.parseFile();

        var sem = Sema.init(&pool, &diags, alloc);
        try sem.analyze(top);
        if (diags.hasErrors()) return error.SemaFailed;

        var lower = Lowerer.init(&pool, &sem, alloc);
        const module = try lower.lower(top);

        var buf: std.ArrayListUnmanaged(u8) = .{};
        try emitModule(module, buf.writer(alloc));

        return .{ .arena = arena, .out = try buf.toOwnedSlice(alloc) };
    }

    fn output(self: *const TestCodegen) []const u8 { return self.out; }

    fn has(self: *const TestCodegen, needle: []const u8) bool {
        return std.mem.indexOf(u8, self.out, needle) != null;
    }

    fn deinit(self: *TestCodegen) void { self.arena.deinit(); }
};

test "emitRuntimeHeader produces valid content" {
    var buf: std.ArrayListUnmanaged(u8) = .{};
    defer buf.deinit(testing.allocator);
    try emitRuntimeHeader(buf.writer(testing.allocator));
    const out = buf.items;
    try testing.expect(std.mem.indexOf(u8, out, "ChasmCtx") != null);
    try testing.expect(std.mem.indexOf(u8, out, "chasm_alloc") != null);
    try testing.expect(std.mem.indexOf(u8, out, "chasm_clear_frame") != null);
}

