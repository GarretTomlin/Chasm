/// C code emitter.
///
/// Takes a fully-lowered `IrModule` and emits a single C99 translation unit.
/// A companion `emitRuntimeHeader` produces `chasm_rt.h`, which the emitted
/// source `#include`s.
///
/// Lifetime threading:
///   Every generated function receives a `ChasmCtx *ctx` as its first
///   parameter.  Frame allocations use `ctx->frame`, script `ctx->script`,
///   persistent `ctx->persistent`.  Primitive values (int, float, bool, atom,
///   string) use C value semantics; `promote` on a scalar is a plain copy.
///   Future: structs will call `chasm_arena_copy` for arena-to-arena moves.
///
/// Naming conventions:
///   - Chasm functions  → `chasm_<name>`
///   - Module @attrs    → `g_<name>`
///   - IR temporaries   → `t<id>` (e.g., `t0`, `t1`)
///   - Labels           → `L<id>` (e.g., `L0`, `L1`)

const std    = @import("std");
const ir     = @import("ir");
const reload = @import("reload");

const IrModule   = ir.IrModule;
const IrFunction = ir.IrFunction;
const IrAttr     = ir.IrAttr;
const IrParam    = ir.IrParam;
const Instr      = ir.Instr;
const Temp       = ir.Temp;
const TempId     = ir.TempId;
const LabelId    = ir.LabelId;
const BinaryOp   = ir.BinaryOp;
const UnaryOp    = ir.UnaryOp;
const Lifetime   = @import("runtime").Lifetime;

