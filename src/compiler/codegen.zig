fn isExternFn(name: []const u8, extern_fns: []const ir.IrExternFn) ?[]const u8 {
    for (extern_fns) |ef| {
        if (std.mem.eql(u8, ef.name, name)) return ef.c_name;
    }
    return null;
}

fn emitInstr(instr: Instr, temps: []const Temp, extern_fns: []const ir.IrExternFn, writer: anytype, indent: usize) !void {
    // Emit `indent * 4` spaces.
    const spaces = "                "; // 16 spaces
    const pad = spaces[0 .. @min(indent * 4, spaces.len)];

    switch (instr) {
        // ---- Literals ------------------------------------------------------
        .const_int    => |i| try writer.print("{s}t{d} = {d};\n",     .{ pad, i.dest, i.value }),
        .const_float  => |i| try writer.print("{s}t{d} = {d};\n",     .{ pad, i.dest, i.value }),
        .const_bool   => |i| try writer.print("{s}t{d} = {s};\n",     .{ pad, i.dest, if (i.value) "true" else "false" }),
        .const_string => |i| try writer.print("{s}t{d} = \"{s}\";\n", .{ pad, i.dest, i.value }),
        .const_atom   => |i| try writer.print("{s}t{d} = \"{s}\";\n", .{ pad, i.dest, i.value }),

        // ---- Data movement -------------------------------------------------
        .copy    => |i| try writer.print("{s}t{d} = t{d};\n", .{ pad, i.dest, i.src }),
        .promote => |i| {
            // Scalars: plain copy (arena semantics for structs deferred).
            if (i.from == i.to) {
                try writer.print("{s}t{d} = t{d}; /* same-lt copy */\n", .{ pad, i.dest, i.src });
            } else {
                try writer.print("{s}t{d} = chasm_promote_scalar(t{d}); /* {s} -> {s} */\n",
                    .{ pad, i.dest, i.src, @tagName(i.from), @tagName(i.to) });
            }
        },

        // ---- Operations ----------------------------------------------------
        .call => |i| {
            // Check if this is an extern function call
            if (isExternFn(i.callee, extern_fns)) |c_name| {
                // Extern function: emit c_name(args...) directly, no chasm_ prefix, no ctx
                if (i.dest == ir_mod.invalid_temp) {
                    try writer.print("{s}{s}(", .{ pad, c_name });
                } else {
                    try writer.print("{s}t{d} = {s}(", .{ pad, i.dest, c_name });
                }
                for (i.args, 0..) |a, ai| {
                    if (ai > 0) try writer.print(", ", .{});
                    try writer.print("t{d}", .{a});
                }
                try writer.print(");\n", .{});
            } else {
                // print/log dispatch: pick the right C overload by argument type.
                const callee = blk: {
                    const is_io = std.mem.eql(u8, i.callee, "print") or
                                  std.mem.eql(u8, i.callee, "log");
                    if (is_io and i.args.len == 1) {
                        const arg_id = i.args[0];
                        const arg_ty = if (arg_id < temps.len) temps[arg_id].type_id else 0;
                        const suffix: []const u8 = switch (arg_ty) {
                            2 => "_f",  // T_FLOAT
                            3 => "_b",  // T_BOOL
                            4, 5 => "_s", // T_STRING, T_ATOM
                            else => "",   // T_INT / T_UNKNOWN
                        };
                        break :blk try std.fmt.allocPrint(std.heap.page_allocator,
                            "{s}{s}", .{ i.callee, suffix });
                    }
                    break :blk i.callee;
                };
                // Determine which args need to be passed by pointer (array builtins)
                const array_builtins = [_][]const u8{
                    "array_push", "array_pop", "array_get", "array_set",
                    "array_len", "array_clear",
                };
                var is_array_builtin = false;
                for (array_builtins) |ab| {
                    if (std.mem.eql(u8, callee, ab)) { is_array_builtin = true; break; }
                }
                if (i.dest == ir_mod.invalid_temp) {
                    try writer.print("{s}chasm_{s}(ctx", .{ pad, callee });
                } else {
                    try writer.print("{s}t{d} = chasm_{s}(ctx", .{ pad, i.dest, callee });
                }
                for (i.args, 0..) |a, arg_idx| {
                    // First array arg of array builtins needs '&'
                    if (is_array_builtin and arg_idx == 0) {
                        try writer.print(", &t{d}", .{a});
                    } else {
                        try writer.print(", t{d}", .{a});
                    }
                }
                try writer.print(");\n", .{});
            }
        },
        .binary => |i| try writer.print("{s}t{d} = t{d} {s} t{d};\n",
            .{ pad, i.dest, i.left, cBinaryOp(i.op), i.right }),
        .unary  => |i| try writer.print("{s}t{d} = {s}t{d};\n",
            .{ pad, i.dest, cUnaryOp(i.op), i.operand }),

        // ---- Memory --------------------------------------------------------
        .field_get => |i| try writer.print("{s}t{d} = t{d}.{s};\n",   .{ pad, i.dest, i.object, i.field }),
        .field_set => |i| try writer.print("{s}t{d}.{s} = t{d};\n",   .{ pad, i.object, i.field, i.src }),
        .load_attr  => |i| try writer.print("{s}t{d} = g_{s};\n",     .{ pad, i.dest, i.name }),
        .store_attr => |i| try writer.print("{s}g_{s} = t{d};\n",     .{ pad, i.name, i.src }),

        // ---- Control flow --------------------------------------------------
        .label  => |id| try writer.print("L{d}:;\n", .{id}), // trailing ; avoids empty-label C warning
        .branch => |i| try writer.print("{s}if (t{d}) goto L{d}; else goto L{d};\n",
            .{ pad, i.cond, i.then_lbl, i.else_lbl }),
        .jump   => |id| try writer.print("{s}goto L{d};\n", .{ pad, id }),
        .ret    => |t|  try writer.print("{s}return t{d};\n", .{ pad, t }),
        .ret_void =>    try writer.print("{s}return;\n", .{pad}),
    }
}

